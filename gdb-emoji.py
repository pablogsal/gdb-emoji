class EmojiPrinter(object):
    def __init__(self, val):
        self.val = val

    def to_string(self):
        address = int(self.val)
        emoji = EMOJI_UNICODE[hash(address) % len(EMOJI_UNICODE)]
        return "{emoji} ({address})".format(emoji=emoji, address=hex(address))


def is_pointer(value):
    typename = str(value.type.strip_typedefs().unqualified())

    # I know, right?
    if "*" not in typename:
        return None
    try:
        int(value)
    except ValueError:
        return None
    return EmojiPrinter(value)


